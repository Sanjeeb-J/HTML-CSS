// string methods = allow you to manipulate and work with text (strings)

let user = "Sanjeeb";

// charAt() = find "letter" at particular index
console.log(user.charAt(0));

// lastIndexOf() = find "index" of a particular letter
console.log(user.indexOf("a"));

// lastIndexOf() = find last "index" of a particular letter
console.log(user.lastIndexOf("b"));

// length = find length of string
console.log(user.length);
